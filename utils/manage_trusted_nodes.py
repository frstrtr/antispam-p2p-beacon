def main():
    if len(sys.argv) < 2:
        show_usage()
        return
    
    command = sys.argv[1].lower()
    
    try:
        if command in ['help', '--help', '-h']:
            show_usage()
        elif command == 'add':
            if len(sys.argv) < 3:
                print("âŒ Error: Node key required for add command")
                print("Usage: python manage_trusted_nodes.py add <node_key>")
                sys.exit(1)
            add_trusted_node(sys.argv[2])
        elif command == 'remove':
            if len(sys.argv) < 3:
                print("âŒ Error: Node key required for remove command")
                print("Usage: python manage_trusted_nodes.py remove <node_key>")
                sys.exit(1)
            remove_trusted_node(sys.argv[2])
        elif command == 'list':
            list_trusted_nodes()
        elif command == 'clear':
            clear_trusted_nodes()
        elif command == 'status':
            show_status()
        else:
            print(f"âŒ Unknown command: {command}")
            show_usage()
            sys.exit(1)
    except KeyboardInterrupt:
        print("\nâŒ Operation cancelled")
        sys.exit(1)
    except Exception as e:
        print(f"âŒ Error: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
